package com.study.models;

public class SortedSetModel {

	private String sortedSetName;
	private String sortedSetSize;
	private String sortedSetDataType;
	public String getSortedSetName() {
		return sortedSetName;
	}
	public void setSortedSetName(String sortedSetName) {
		this.sortedSetName = sortedSetName;
	}
	public String getSortedSetSize() {
		return sortedSetSize;
	}
	public void setSortedSetSize(String sortedSetSize) {
		this.sortedSetSize = sortedSetSize;
	}
	public String getSortedSetDataType() {
		return sortedSetDataType;
	}
	public void setSortedSetDataType(String sortedSetDataType) {
		this.sortedSetDataType = sortedSetDataType;
	}
	
}
