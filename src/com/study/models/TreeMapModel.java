package com.study.models;

public class TreeMapModel {

	private String treeMapName;
	private String treeMapSize;
	private String treeMapDataType;
	public String getTreeMapName() {
		return treeMapName;
	}
	public void setTreeMapName(String treeMapName) {
		this.treeMapName = treeMapName;
	}
	public String getTreeMapSize() {
		return treeMapSize;
	}
	public void setTreeMapSize(String treeMapSize) {
		this.treeMapSize = treeMapSize;
	}
	public String getTreeMapDataType() {
		return treeMapDataType;
	}
	public void setTreeMapDataType(String treeMapDataType) {
		this.treeMapDataType = treeMapDataType;
	}
	
}
